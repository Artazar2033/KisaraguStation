#include "map.h"

Map::Map(string abs[HEIGHT_MAP], int numb) {
    for (int i = 0; i < HEIGHT_MAP; i++) {
        TileMap[i] = abs[i];
    }
    isPassed = false;
    number = numb;
}

Map::Map(int numb) { isPassed = false; number = numb; }

void Map::randomMapGenerate() {//рандомно расставл€ем камни
    int randomElementX = 0;//переменна€ дл€ хранени€ случайного элемента по горизонтали
    int randomElementY = 0;//переменна€ дл€ хранени€ случайного элемента по вертикали
    int countStone = 5;//количество камней
    while (countStone > 0) {
        randomElementX = 1 + rand() % (WIDTH_MAP - 1);//псевдослучайное значение по УxФ от 1 до
        //ширина карты-1. ќграничение введено чтобы не получать числа бордюра карты
        randomElementY = 1 + rand() % (HEIGHT_MAP - 1);//по УyФ
        if (TileMap[randomElementY][randomElementX] == ' ') {//если встретили символ пробел,
            TileMap[randomElementY][randomElementX] = 's'; //то ставим туда камень.
            countStone--;
        }
    }
}

string* Map::GetTileMap() {
    return TileMap;
}

void Map::draw(Sprite* s_map, RenderWindow* window) { //сюда можно подать указатель на другой спрайт,
    //тогда карта будет выгл€деть по-другому
    for (int i = 0; i < HEIGHT_MAP; i++)
        for (int j = 0; j < WIDTH_MAP; j++)
        {
            if (TileMap[i][j] == ' ') s_map->setTextureRect(IntRect(0, 0, 32, 32));//пол
            if (TileMap[i][j] == 's') s_map->setTextureRect(IntRect(32, 0, 32, 32));//монетка
            if (TileMap[i][j] == '0') s_map->setTextureRect(IntRect(64, 0, 32, 32));//камень
            if (TileMap[i][j] == 'f') s_map->setTextureRect(IntRect(96, 0, 32, 32));//€довита€ лужа
            if (TileMap[i][j] == 'h') s_map->setTextureRect(IntRect(128, 0, 32, 32));//еда

            if (TileMap[i][j] == 'k') s_map->setTextureRect(IntRect(291, 0, 32, 32));//ключ !!!!!!!!!!!!!!!!!!!

            if ((TileMap[i][j] == '?') || (TileMap[i][j] == '/')) s_map->setTextureRect(IntRect(162, 0, 32, 32));
            //вертикальна€ дверь, верх
            if ((TileMap[i][j] == '!') || (TileMap[i][j] == '1')) s_map->setTextureRect(IntRect(193, 0, 32, 32));
            //вертикальна€ дверь, низ
            if ((TileMap[i][j] == '(') || (TileMap[i][j] == '9')) s_map->setTextureRect(IntRect(225, 0, 32, 32));
            //горизонтальна€ дверь, лево
            if ((TileMap[i][j] == ')') || (TileMap[i][j] == '-')) s_map->setTextureRect(IntRect(257, 0, 32, 32));
            //горизонтальна€ дверь, право
            if (TileMap[i][j] == '_') s_map->setTextureRect(IntRect(322, 0, 32, 32));
            //дверь в сейфрум (!!!обновить спрайт!!!)
            if (TileMap[i][j] == 'd') s_map->setTextureRect(IntRect(257, 0, 32, 32));//дверь к боссу (???)
            s_map->setPosition(j * 32, i * 32);
            (*window).draw(*s_map);
        }
}
